package ex_08_For_Loop;

public class For_Loop_If_Loop_Example5 {
    public static void main(String[] args) {
        for(int raja_age=0;raja_age<3;raja_age++)
        {
            if(raja_age<2)
            {
                System.out.println("Not allowed to vote");
                System.out.println(raja_age);
            }
            else
            {
                System.out.println("Not allowed to vote");
            }
        }
    }
}
