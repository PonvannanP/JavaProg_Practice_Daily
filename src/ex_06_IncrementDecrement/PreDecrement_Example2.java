package ex_06_IncrementDecrement;

public class PreDecrement_Example2 {
    public static void main(String[] args) {
        int a=53;
        int result_a=--a;//
        System.out.println(result_a);//First decrements the value and then assigns the value which is 52
        System.out.println(a);// a value will also be 52

    }
}
