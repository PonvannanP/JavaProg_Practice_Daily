package Whileloop;

public class While_Loop_Example1 {
    public static void main(String[] args) {
        int i = 0;//Initilization will be outside
        while (i < 10) //Condition(must be true to enter into the loop)
        {
            System.out.println("Start the program");// Piece of statements
            System.out.println(i);
            i++;//Updation-> Increment/Decrement in the last
        }
    }
}
