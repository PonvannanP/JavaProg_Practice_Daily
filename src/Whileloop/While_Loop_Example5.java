package Whileloop;

public class While_Loop_Example5 {
    public static void main(String[] args) {
        int i1=10;
        while(i1>=1)// it will execute for 10 times once i becomes 0 it will
            // come out of the loop
        {
            System.out.println(i1);
            i1--;
        }
    }
}
