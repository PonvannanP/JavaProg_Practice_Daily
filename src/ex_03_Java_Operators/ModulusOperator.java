package ex_03_Java_Operators;

public class ModulusOperator {
    public static void main(String[] args) {
        int a=20;
        int b=3;
        System.out.println(a%b);// 20 divided by 3 Quotient is:6 and remainder is: 2,
        // Modulus Operator will only prints the remainder which is 2
    }



}
