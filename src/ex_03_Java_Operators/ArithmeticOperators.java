package ex_03_Java_Operators;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int a=20;
        int b=10;
        float c=1.5F;//for float it will be represented as either 'f' or 'F'
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c);//If you divide a float value it will give float digit
    }
}
