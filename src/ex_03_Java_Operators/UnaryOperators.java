package ex_03_Java_Operators;

public class UnaryOperators {
    public static void main(String[] args) {
        int a=10;
        int b=-20;
        int result=a+b;//-20+10
        System.out.println(result);//-10 will be printed
        int c=-1;
        c=c+1;//-1+1
        System.out.println(c);//0 will be printed
    }
}
