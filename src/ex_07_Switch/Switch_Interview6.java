package ex_07_Switch;

public class Switch_Interview6 {
    public static void main(String[] args) {
        char ch = 'C';
        switch (ch) {
            case 'A':
                System.out.println("67");
                break;
            case 'B':
                System.out.println("B class");
                break;
            default:
                System.out.println("Hellllooo");//default will execute
                break;

        }
    }
}
